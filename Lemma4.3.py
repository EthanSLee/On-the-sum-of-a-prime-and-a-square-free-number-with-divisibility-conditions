from sympy import prevprime, primerange, nextprime, factorint, isprime

def coprime(a,b):
    fi_A = factorint(a)
    fi_B = factorint(b)
    for p in fi_A:
        if p in fi_B.keys():
            return False
    return True

def nextcoprimesquarefree(y,coprime_to):
    i = 1
    while i > 0:
        fi = factorint(y + i)
        condition_met = [coprime(j,y+i) for j in coprime_to]
        if max(fi.values()) == 1 and False not in condition_met:
            return y + i
        i += 1

def is_good(n):
    sf = 2 #the first nontrivial squarefree
    cpt = []
    while sf < n:
        if isprime(n-sf) == True:
            cpt.append(sf)
        if len(cpt) < 4:
            sf = nextcoprimesquarefree(sf, cpt)
        else:
            return True, cpt
    return False, cpt

def verify_ft_semiprimes_small(from_me, to_me):
    not_verified = []
    m = from_me
    while m <= to_me+1:
        ig = is_good(m)
        if ig[0] == False:
            print(m)
            not_verified.append(m)
        m += 2
    return not_verified

def verify_ft_semiprimes_quick(from_me, to_me):
    small_primes = set()
    p = 2
    while p < 500:
        small_primes.add(p)
        p = nextprime(p)
    
    pc_primes = set()
    p = prevprime(from_me - 10**3)
    while p < to_me:
        pc_primes.add(p)
        p = nextprime(p)
    
    num_representations = {}
    for q in pc_primes:
        for p in small_primes:
            m = p + q
            if m not in num_representations:
                num_representations[m] = 1
            else:
                num_representations[m] += 1
    #print("Generated!")
    
    not_verified = []
    m = from_me
    while m <= to_me:
        if m not in num_representations or num_representations[m] < 2:
            reps = []
            q = 2
            while 2*q < m and len(reps) < 2:
                if m-q in pc_primes:
                    #print(q,m-q)
                    reps.append((q,m-q))
                q = nextprime(q)
            #print(m, reps)
            if len(reps) < 2:
                not_verified.append(m)
        m += 2
    return not_verified

